public class HypothesisCostItemConfiguration : IEntityTypeConfiguration<HypothesisCostItem>
{
    public void Configure(EntityTypeBuilder<HypothesisCostItem> builder)
    {
        builder.ToTable("HypothesisCostItems");

        builder.HasKey(i => i.Id);
        builder.Property(i => i.Id).HasDefaultValueSql("NEWSEQUENTIALID()").ValueGeneratedNever();

        builder.Property(i => i.HypothesisAnalysisId).IsRequired();
        builder.Property(i => i.Category).IsRequired().HasConversion<int>();
        builder.Property(i => i.Kind).IsRequired().HasConversion<int>();
        builder.Property(i => i.ModelName).HasMaxLength(200);
        builder.Property(i => i.Description).IsRequired().HasMaxLength(500);
        builder.Property(i => i.DisplaySequence).IsRequired();

        builder.Property(i => i.RateAmount).HasPrecision(17, 2);
        builder.Property(i => i.Quantity).HasPrecision(13, 2);
        builder.Property(i => i.Amount).HasPrecision(17, 2);
        builder.Property(i => i.RatePercent).HasPrecision(5, 2);
        builder.Property(i => i.CategoryRatio).HasPrecision(5, 2);

        // ── CostOfBuilding categorisation ────────────────────────────────
        builder.Property(i => i.IsBuilding)
            .IsRequired()
            .HasDefaultValue(true);

        builder.Property(i => i.DepreciationMethod)
            .IsRequired()
            .HasMaxLength(16)
            .HasDefaultValue("Gross");

        // ── CostOfBuilding B-fields ───────────────────────────────────────
        builder.Property(i => i.Area).HasPrecision(13, 2);                    // FSD B01
        builder.Property(i => i.PricePerSqM).HasPrecision(17, 2);             // FSD B02
        builder.Property(i => i.PriceBeforeDepreciation).HasPrecision(17, 2); // FSD B03
        // Year (int?) — no precision config needed                            // FSD B04
        builder.Property(i => i.AnnualDepreciationPercent).HasPrecision(5, 2);// FSD B05
        builder.Property(i => i.TotalDepreciationPercent).HasPrecision(5, 2); // FSD B06
        builder.Property(i => i.DepreciationAmount).HasPrecision(17, 2);      // FSD B07
        builder.Property(i => i.ValueAfterDepreciation).HasPrecision(17, 2);  // FSD B08

        // ── Period depreciation child rows ────────────────────────────────
        builder.HasMany(i => i.DepreciationPeriods)
            .WithOne()
            .HasForeignKey(p => p.CostItemId)
            .OnDelete(DeleteBehavior.Cascade)
            .Metadata.PrincipalToDependent!.SetPropertyAccessMode(PropertyAccessMode.Field);

        builder.HasIndex(i => i.HypothesisAnalysisId);
        builder.HasIndex(i => new { i.HypothesisAnalysisId, i.Category });
    }
}

